package kr.ac.hs.dto;

import java.util.Date;

/**
 * 강의계획 VO
 * @author PC-01
 *
 */
public class LecturePlanVO {

	private Date lecture_semester;
	private String class_no;
	private String lecture_summary;
	private String lecture_goal;
	private String textbook;

	public Date getLecture_semester() {
		return lecture_semester;
	}

	public void setLecture_semester(Date elcture_semester) {
		this.lecture_semester = elcture_semester;
	}

	public String getClass_no() {
		return class_no;
	}

	public void setClass_no(String class_no) {
		this.class_no = class_no;
	}

	public String getLecture_summary() {
		return lecture_summary;
	}

	public void setLecture_summary(String lecture_summary) {
		this.lecture_summary = lecture_summary;
	}

	public String getLecture_goal() {
		return lecture_goal;
	}

	public void setLecture_goal(String lecture_goal) {
		this.lecture_goal = lecture_goal;
	}

	public String getTextbook() {
		return textbook;
	}

	public void setTextbook(String textbook) {
		this.textbook = textbook;
	}
}

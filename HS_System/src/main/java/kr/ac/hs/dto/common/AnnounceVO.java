package kr.ac.hs.dto.common;

public class AnnounceVO {

	private String anno_no;
	private String account_no;
	private String anno_check_cd;
	private String anno_content;
	private String mcode;
	
	public String getAnno_no() {
		return anno_no;
	}
	public void setAnno_no(String anno_no) {
		this.anno_no = anno_no;
	}
	public String getAccount_no() {
		return account_no;
	}
	public void setAccount_no(String account_no) {
		this.account_no = account_no;
	}
	public String getAnno_check_cd() {
		return anno_check_cd;
	}
	public void setAnno_check_cd(String anno_check_cd) {
		this.anno_check_cd = anno_check_cd;
	}
	public String getAnno_content() {
		return anno_content;
	}
	public void setAnno_content(String anno_content) {
		this.anno_content = anno_content;
	}
	public String getMcode() {
		return mcode;
	}
	public void setMcode(String mcode) {
		this.mcode = mcode;
	}
	
	
}

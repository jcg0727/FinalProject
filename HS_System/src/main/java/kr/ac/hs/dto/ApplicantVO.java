package kr.ac.hs.dto;

public class ApplicantVO {
	private int applicant_no; //지원자번호
	private String picture; //사진경로
	private String name; //이름
	private int reg_no; //주민번호
	private String appli_dept; //지원학과
	private String email; //이메일주소
	private String address;//주소
	private String detail_add; //상세주소
	private int phone; //전화번호
	private String pass_whether; //합격여부
	private String regist_check; //등록여부
	private String dept_cd; //학과코드
	
	public int getApplicant_no() {
		return applicant_no;
	}
	public void setApplicant_no(int applicant_no) {
		this.applicant_no = applicant_no;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getReg_no() {
		return reg_no;
	}
	public void setReg_no(int reg_no) {
		this.reg_no = reg_no;
	}
	public String getAppli_dept() {
		return appli_dept;
	}
	public void setAppli_dept(String appli_dept) {
		this.appli_dept = appli_dept;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDetail_add() {
		return detail_add;
	}
	public void setDetail_add(String detail_add) {
		this.detail_add = detail_add;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int phone) {
		this.phone = phone;
	}
	public String getPass_whether() {
		return pass_whether;
	}
	public void setPass_whether(String pass_whether) {
		this.pass_whether = pass_whether;
	}
	public String getRegist_check() {
		return regist_check;
	}
	public void setRegist_check(String regist_check) {
		this.regist_check = regist_check;
	}
	public String getDept_cd() {
		return dept_cd;
	}
	public void setDept_cd(String dept_cd) {
		this.dept_cd = dept_cd;
	}
	
	
}

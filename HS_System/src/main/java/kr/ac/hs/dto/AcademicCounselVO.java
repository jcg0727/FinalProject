package kr.ac.hs.dto;

import java.util.Date;

public class AcademicCounselVO {
	
	private Date register_date; // 신청일시
	private String student_no;  // 학번
	private String consl_categ_no; // 상담분류번호

	public Date getRegister_date() {
		return register_date;
	}
	public void setRegister_date(Date register_date) {
		this.register_date = register_date;
	}
	public String getStudent_no() {
		return student_no;
	}
	public void setStudent_no(String student_no) {
		this.student_no = student_no;
	}
	public String getConsl_categ_no() {
		return consl_categ_no;
	}
	public void setConsl_categ_no(String consl_categ_no) {
		this.consl_categ_no = consl_categ_no;
	}
	
	
}

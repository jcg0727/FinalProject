package kr.ac.hs.dto.sugang;

import java.util.Date;

public class ApplyLessonVO {
	
	private String class_no;
	private String title;
	private Date class_start_day;
	private Date class_end_day;
	
	public String getClass_no() {
		return class_no;
	}
	public void setClass_no(String class_no) {
		this.class_no = class_no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Date getClass_start_day() {
		return class_start_day;
	}
	public void setClass_start_day(Date class_start_day) {
		this.class_start_day = class_start_day;
	}
	public Date getClass_end_day() {
		return class_end_day;
	}
	public void setClass_end_day(Date class_end_day) {
		this.class_end_day = class_end_day;
	}

	
	

}
